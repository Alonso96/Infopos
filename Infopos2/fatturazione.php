      <p class="desc">
    <strong>  La fatturazione elettronica tra privati (ovvero tra partite iva), anticipata dalla fatturazione elettronica verso la Pubblica Amministrazione, entrerà effettivamente in vigore il 1° gennaio 2019.</strong>

Molte attività commerciali e del settore ristorativo si stanno chiedendo a questo punto come poter adempire alla normativa senza però imbattersi in problematiche di costi e tempo. Partiamo dicendo che con la fatturazione elettronica entrano in campo tre momenti consequenziali importanti affinché la fattura sia determinata valida: l’emissione, la trasmissione e la conservazione.

Ogni titolare di qualunque attività commerciale dovrà emettere le fatture in formato XML (eXtensible Markup Language), un linguaggio informatico che consente di definire e controllare il significato degli elementi contenuti in un documento, verificando così le informazioni ai fini dei controlli previsti per legge.

Una volta prodotto il file della fattura elettronica, questa deve essere inviata telematicamente verso SdI, il Sistema di Interscambio, mediante uno dei canali previsti (PEC, FTP, cooperazione applicativa), che successivamente provvederà alla consegna della fattura al cliente.

L’SdI che è il sistema informatico gestito dall’Agenzia delle Entrate, ha 3 importanti funzioni:

    ricevere le fatture sotto forma di file in formato XLM, l’unico accettato per Legge;
    effettuare tutti i controlli necessari sui file ricevuti;
    inoltrare le fatture ricevute ai clienti, destinatari delle fatture.

Infine, il titolare dell’attività avrà l’obbligo di conservare le fatture per una durata di 10 anni, o presso l’Agenzia per le Entrate oppure presso Conservatori Accreditati, ovvero aziende accreditate dall’Agenzia per l’Italia Digitale (AgID).


<img src="images/fat2.png" alt="schema" style="width:100% ">

Tutto questo iter può essere gestito in autonomia con l’utilizzo dei portali dell’Agenzia delle Entrate o di Infocamere, in questo caso la fatturazione elettronica sarà gestita gratuitamente, a discapito però dell’impiego di tempo da parte del titolare e di possibili errori che possono portare a ritardi dei pagamenti.

Con i nostri gestionali  sarà possibile emettere automaticamente la fattura nel formato XML, quello richiesto dall’Agenzia delle Entrate, così si ridurranno tutti gli errori derivanti dalla gestione manuale delle fatture elettroniche e il tempo impiegato per la loro produzione. Oggi offriamo una soluzione completa per adempire facilmente e con costi decisamente contenuti all’obbligo della fatturazione elettronica. Inoltre a differenza di altre soluzioni che, pur facilitando l’emissione richiedono comunque più passaggi per la creazione della fattura a norma, <strong>InfoPos propone registratori di cassa touch screen già predisposti per questa imminente novità</strong>.L’altra soluzione è quella di servirsi di software, che con diverse funzionalità, costi e servizi offrono una soluzione più semplice e veloce per l’emissione, la trasmissione e la conservazione a norma della fattura elettronica e soprattutto riducendo al minimo i rischi di errore.

I punti cassa, oltre ad adempire alla normativa nell’emissione del file XML per la fatturazione elettronica, facilitano la gestione delle comande del locale e velocizzano le operazioni in cassa, lasciando al ristoratore il tempo per occuparsi di ciò che è davvero importante: i clienti.

Tutti i registratori di cassa professionali  sono dotati di moltissime e semplici funzioni progettate per il settore della ristorazione e possono essere personalizzate in base alle esigenze del singolo ristoratore.

Con l’ormai imminente introduzione dell’obbligo di fatturazione elettronica, per molti imprenditori è quindi questo, il miglior momento per decidere di automatizzare la gestione del proprio locale e velocizzare le operazioni in cassa attraverso l’uso di software professionali per ristoranti e per bar.

Per maggiori informazioni e per una consulenza gratuita per scegliere la soluzione migliore per la vostra attività, lo staff di InfoPos è a vostra disposizione.
