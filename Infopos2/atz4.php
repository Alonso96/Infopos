      <p class="desc">
    
  
ATZ4 è il palmare comande progettato da CEI Systems per i professionisti della ristorazione, come ad esempio ristoranti, bar, pizzerie e pub.
Questo palmare per la raccolta delle ordinazioni è un vero e proprio mix di tecnologia e semplicità: in soli 15 centimetri, presenta un ampio display con un dispositivo di luminosità che si autoregola per permettere al cameriere una visione ottimale e antiriflesso, una batteria al litio integrata di lunga durata, oltre a un sensore che ne permette lo spegnimento automatico dopo un determinato tempo di inutilizzo.
Al fine di garantire una lunga durata del palmare comande, è realizzato con materiali che permettono di resiste a forti urti. Inoltre, per proteggerlo ulteriormente, la CEI ha previsto un guscio protettivo in morbido lattice fornito insieme al taccuino al momento dell’acquisto.
È inoltre uno dei pochi palmari in commercio ad essere davvero “dedicato”, ovvero pensato e realizzato esclusivamente per il settore della ristorazione.
ATZ4 è estremamente facile da utilizzare ed è esente da tutte le problematiche delle App e relativi sistemi operativi, tipici di altri palmari per comande; inoltre è maneggevole, piccolo e ricco di funzionalità: è dotato ad esempio di tecnologia RFID che gli consente di riconoscere in automatico il cliente, l’operatore che si appresta ad utilizzarlo, il numero del tavolo per l’addebito delle comande, velocizzando tutte le operazioni di cassa e di gestione delle comande.
La nuova modalità di addebito del taccuino elettronico permette di accedere alla pagina delle opzioni con pochi passaggi e offre una più ampia possibilità di modifica dei piatti: in questo modo anche le richieste più complicate possono essere gestite con grande rapidità con il palmare comande ATZ4.
Collegabile tramite Wi-Fi a tutti i registratori di cassa CEI è lo strumento perfetto per un servizio veloce e accurato, anche nei picchi di maggiore affluenza nel locale. 
         		
